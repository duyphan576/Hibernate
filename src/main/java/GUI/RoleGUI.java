    private void btnSearchbtnSearch(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchbtnSearch
        // TODO add your handling code here:
        Role role = roleBll.getRole(Integer.parseInt(txtKey.getText()));
        DefaultTableModel model = (DefaultTableModel) tblRole.getModel();
        model.setRowCount(0);
        model.addRow(new Object[]{
            role.getRoleID(), role.getRoleName()
        });
    }//GEN-LAST:event_btnSearchbtnSearch

    public void showTable() {
        List listCate = roleBll.loadRole();
        Object[][] datamodel;
        datamodel = roleBll.converRole(listCate);
        String[] title = {"ID", "Name"};
        DefaultTableModel model = new DefaultTableModel(datamodel, title);
        tblRole.setModel(model);
    }

