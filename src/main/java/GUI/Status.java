package GUI;

/**
 *
 * @author ACER NITRO 5
 */
public class Status {

    private int value;
    private String title;

    public Status(int value, String title) {
        this.value = value;
        this.title = title;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
    
    @Override
    public String toString() {
        return title;
    }
}
